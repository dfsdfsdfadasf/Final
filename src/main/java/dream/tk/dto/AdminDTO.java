package dream.tk.dto;

public class AdminDTO {


	private String label;
	private int counts;
	
	public AdminDTO() {
		super();
	}

	public AdminDTO(String label, int counts) {
		super();
		this.label = label;
		this.counts = counts;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public int getCounts() {
		return counts;
	}

	public void setCounts(int counts) {
		this.counts = counts;
	}

	
	
	

	
	
	
	
	
	
	
}
